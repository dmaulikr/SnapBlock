import Foundation
import UIKit

extension UIColor {
  
  public class var pixelEmpty: UIColor {
    return UIColor.init(red: 239/255.0, green: 239/255.0, blue: 239/255.0, alpha: 1)
  }
  
  public class var pixelFilled: UIColor {
    return UIColor.init(red: 170/255.0, green: 170/255.0, blue: 170/255.0, alpha: 1)
  }
  
  public class var pixelHover: UIColor {
    return UIColor.init(red: 170/255.0, green: 170/255.0, blue: 170/255.0, alpha: 0.5)
  }
}


